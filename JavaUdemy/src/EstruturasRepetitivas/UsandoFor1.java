package EstruturasRepetitivas;

public class UsandoFor1 {

	public static void main(String[] args) {
		
		// Contagem
		
		for(int i=0 ; i<5 ; i++) {
			
			System.out.println("O valor de i : " + i );
		}
		
		System.out.println();
		
		// Contagem regressiva

		for (int j = 5 ; j>0 ; j--) {
			
			System.out.println("O valor de i: " + j);
		}		
	}

}
